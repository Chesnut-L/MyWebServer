//从状态机，用于分析出一行内容
//返回值为行的读取状态，有LINE_OK,LINE_BAD,LINE_OPEN
http_conn::LINE_STATUS http_conn::parse_line()
{
    char temp;
    //m_read_idx 是已读到的数据长度 m_checked_idx已经初始化为0
    for (; m_checked_idx < m_read_idx; ++m_checked_idx)
    {   
        // 要分析的字符
        temp = m_read_buf[m_checked_idx];
        // 到 \r 可能是完整的行或者是不完整的行
        if (temp == '\r')
        {   // 如果\r是末尾 数据不完整，返回line open 继续读数据 （一行标准的数据是\r\n）
            if ((m_checked_idx + 1) == m_read_idx)
                return LINE_OPEN;
                // 读到\n 将\n 改成\0 \0读完了 
            else if (m_read_buf[m_checked_idx + 1] == '\n')
            {
                m_read_buf[m_checked_idx++] = '\0';
                m_read_buf[m_checked_idx++] = '\0';
                return LINE_OK;
            }
            // 不符合返回语法错误
            return LINE_BAD;
        }
        // 有可能是最后一行 上次读到\r 这次继续读到\n 再次接受
        else if (temp == '\n')
        {
            if (m_checked_idx > 1 && m_read_buf[m_checked_idx - 1] == '\r')
            {
                // 前一个是 \r 接受完整
                m_read_buf[m_checked_idx - 1] = '\0';
                m_read_buf[m_checked_idx++] = '\0';
                return LINE_OK;
            }
            return LINE_BAD;
        }
    }
    return LINE_OPEN;
}

//循环读取客户数据，直到无数据可读或对方关闭连接
//非阻塞ET工作模式下，需要一次性将数据读完更新m_read_idx
bool http_conn::read_once()
{   // 读到结尾了
    if (m_read_idx >= READ_BUFFER_SIZE)
    {
        return false;
    }
    int bytes_read = 0;

    //LT读取数据
    if (0 == m_TRIGMode)
    {
        bytes_read = recv(m_sockfd, m_read_buf + m_read_idx, READ_BUFFER_SIZE - m_read_idx, 0);
        // cout << bytes_read << endl;
        m_read_idx += bytes_read;

        if (bytes_read <= 0)
        {
            return false;
        }

        return true;
    }
    //ET读数据
    else
    {
        while (true)
        {
            bytes_read = recv(m_sockfd, m_read_buf + m_read_idx, READ_BUFFER_SIZE - m_read_idx, 0);
            // cout << bytes_read << endl;
            if (bytes_read == -1)
            {
                if (errno == EAGAIN || errno == EWOULDBLOCK)
                    break;
                return false;
            }
            else if (bytes_read == 0)
            {
                return false;
            }
            m_read_idx += bytes_read;
        }
        return true;
    }
}

//解析http请求行，获得请求方法，目标url及http版本号
http_conn::HTTP_CODE http_conn::parse_request_line(char *text)
{   // strpbrk 检索str1中第一个匹配str2中字符的字符，不包含空结束字符
    // cout << "text " <<  text << endl;
    m_url = strpbrk(text, " \t");
    // cout << "m_url " <<  m_url << endl;
    // 没有空格或者\t，报文格式有误
    if (!m_url)
    {
        return BAD_REQUEST;
    }
    // 改为\0取出前面的数据
    *m_url++ = '\0';
    // cout << "m_url after" << m_url << endl;
    // cout << "text after" << text << endl;
    // 现在text就是请求方法
    char *method = text;
    //  cout << "method" << method << endl;
    // 比较字符串
    if (strcasecmp(method, "GET") == 0)
        m_method = GET;
    else if (strcasecmp(method, "POST") == 0)
    {   // cgi标志位置1
        m_method = POST;
        cgi = 1;
    }
    else
        return BAD_REQUEST;
        //size_t strspn(char const *str, char const *group);
        // 就是从str的第一个元素开始往后数，看str中是不是连续往后每个字符都在group中可以找到。
        //到第一个不在gruop的元素为止。看从str第一个开始，前面的字符有几个在group中
        //相当于跳过\t，返回的是匹配个数
    m_url += strspn(m_url, " \t");
    // 就到了版本号取\t前的
    m_version = strpbrk(m_url, " \t");
    if (!m_version)
        return BAD_REQUEST;
    *m_version++ = '\0';
    m_version += strspn(m_version, " \t");
    // 仅支持1.1
    if (strcasecmp(m_version, "HTTP/1.1") != 0)
        return BAD_REQUEST;
        // 针对http和https
    if (strncasecmp(m_url, "http://", 7) == 0)
    {
        m_url += 7;
        m_url = strchr(m_url, '/');
    }

    if (strncasecmp(m_url, "https://", 8) == 0)
    {
        m_url += 8;
        m_url = strchr(m_url, '/');
    }

    if (!m_url || m_url[0] != '/')
        return BAD_REQUEST;

    //当url为/时，显示欢迎界面
    if (strlen(m_url) == 1)
        strcat(m_url, "judge.html");
    // 处理完毕，状态转移到请求头
    m_check_state = CHECK_STATE_HEADER;
    return NO_REQUEST;
}

//解析http请求的一个头部信息
http_conn::HTTP_CODE http_conn::parse_headers(char *text)
{   // 判断是空行还是请求头 
    if (text[0] == '\0')
    {   // 判断有没有请求数据 是post还是get
        if (m_content_length != 0)
        {
            // 是post跳转到解析请求数据
            m_check_state = CHECK_STATE_CONTENT;
            return NO_REQUEST;
        }
        return GET_REQUEST;
    }
    // 解析请求头部的连接字段
    else if (strncasecmp(text, "Connection:", 11) == 0)
    {
        text += 11;
        text += strspn(text, " \t");
        // 跳过空格和\t字符
        if (strcasecmp(text, "keep-alive") == 0)
        {
            // 长连接设置linger
            m_linger = true;
        }
    }
    // 解析内容长度
    else if (strncasecmp(text, "Content-length:", 15) == 0)
    {
        text += 15;
        text += strspn(text, " \t");
        m_content_length = atol(text);
    }
    //解析host
    else if (strncasecmp(text, "Host:", 5) == 0)
    {
        text += 5;
        text += strspn(text, " \t");
        m_host = text;
    }
    else
    {
        LOG_INFO("oop!unknow header: %s", text);
    }
    return NO_REQUEST;
}

//判断http请求是否被完整读入
http_conn::HTTP_CODE http_conn::parse_content(char *text)
{
    // 判断是否有消息体
    if (m_read_idx >= (m_content_length + m_checked_idx))
    {
        text[m_content_length] = '\0';
        //POST请求中最后为输入的用户名和密码
        m_string = text;
        return GET_REQUEST;
    }
    return NO_REQUEST;
}

http_conn::HTTP_CODE http_conn::process_read()
{   // 初始化从状态机状态  请求解析结果 一行的处理后状态
    LINE_STATUS line_status = LINE_OK;
    HTTP_CODE ret = NO_REQUEST;
    char *text = 0;
    // CHECK_STATE_CONTENT 解析消息体
    while ((m_check_state == CHECK_STATE_CONTENT && line_status == LINE_OK) || ((line_status = parse_line()) == LINE_OK))
    {
        text = get_line();
        // 更新开始开始位置 m_checked_idx已经到了下一行的开始
        m_start_line = m_checked_idx;
        LOG_INFO("%s", text);
        // m_check_state被初始化成CHECK_STATE_REQUESTLINE先解析请求行
        switch (m_check_state)
        {
            // 解析 请求行
        case CHECK_STATE_REQUESTLINE:
        {
            ret = parse_request_line(text);
            if (ret == BAD_REQUEST)
                return BAD_REQUEST;
            break;
        }
        case CHECK_STATE_HEADER:
        {
            ret = parse_headers(text);
            if (ret == BAD_REQUEST)
                return BAD_REQUEST;
            else if (ret == GET_REQUEST)
            {
                return do_request();
            }
            break;
        }
        case CHECK_STATE_CONTENT:
        {
            ret = parse_content(text);
            if (ret == GET_REQUEST)
                return do_request();
            line_status = LINE_OPEN;
            break;
        }
        default:
            return INTERNAL_ERROR;
        }
    }
    return NO_REQUEST;
}

